public class LocalNode {

    private double ksi;
    private double eta;

    public LocalNode(double ksi, double eta) {
        this.ksi = ksi;
        this.eta = eta;
    }

    public double getKsi() {
        return ksi;
    }

    public double getEta() {
        return eta;
    }
}
